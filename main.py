#           PYTHON LIST COMPREHENSION
#ordinary syntax:
numbers = []
for x in range(10):
    numbers.append(x)
print(numbers)

#LIST COMPREHENSION syntax:
numbers = [x for x in range(10)]
print(numbers)

#Both get the same result.

